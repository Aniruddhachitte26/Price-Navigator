    private void jButton4IdentifyResourceAssetsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4IdentifyResourceAssetsActionPerformed
        
// TODO add your handling code here:

    String customerName = dropUserType.getSelectedItem().toString();
    
        // Check if the customer name is empty (though it generally shouldn't be from a dropdown)
        if (customerName == null || customerName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Customer name is required.", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }

        // Find the customer in the directory using the selected name
        SuperMarketProfile selectedCustomer = business.getCustomerDirectory().findCustomer(customerName);

        if (selectedCustomer == null) {
            // If no customer matches the input name, show an error message
            JOptionPane.showMessageDialog(null, "Customer not found.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method to prevent further execution
        }

        // If customer is found, proceed with the order process
        ProcessOrder aos = new ProcessOrder(business, selectedCustomer, salesperson, CardSequencePanel);

        CardSequencePanel.add("ManageVulns11", aos);
        ((java.awt.CardLayout) CardSequencePanel.getLayout()).next(CardSequencePanel);
   


    }//GEN-LAST:event_jButton4IdentifyResourceAssetsActionPerformed

