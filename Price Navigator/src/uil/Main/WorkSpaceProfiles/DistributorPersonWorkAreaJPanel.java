    private void btnProductPerformaceReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProductPerformaceReportActionPerformed
        // TODO add your handling code here:
        
   String customerName = (String) dropUserType.getSelectedItem();

if (customerName == null || customerName.trim().isEmpty()) {
    // Display an error message if no customer is selected
    JOptionPane.showMessageDialog(null, "Please select a customer name.", "Input Required", JOptionPane.ERROR_MESSAGE);
} else {
    // Find the customer in the directory using the selected name
    SuperMarketProfile selectedCustomer = business.getCustomerDirectory().findCustomer(customerName);

    if (selectedCustomer == null) {
        // If no customer matches the input name, show an error message
        JOptionPane.showMessageDialog(null, "Customer not found.", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Exit the method to prevent further execution
    }

    // If customer is found, switch to the ProductPerformanceJPanel
    ProductPerformanceJPanel productPerformancePanel = new ProductPerformanceJPanel(business, selectedCustomer, CardSequencePanel);
    CardSequencePanel.add("ProductPerformance", productPerformancePanel);
    ((java.awt.CardLayout) CardSequencePanel.getLayout()).show(CardSequencePanel, "ProductPerformance");
}
    }//GEN-LAST:event_btnProductPerformaceReportActionPerformed

